# Inf
<p>Данный проект является программой, которая находит n-ый член заданной геометрической последовательности.</p>
<br>
<h3>Функции</h3>
<ul>
 <li>Вычисление среднего арифметического всех положительных элементов</li>
 <li>Вычисление суммы элементов, расположенных до максимального элемента</li>
</ul>
